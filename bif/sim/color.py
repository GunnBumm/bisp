def textbold (text):
  return "\033[0;1m"+text+"\033[m"

def textblue (text):
  return "\033[0;34m"+text+"\033[m"

def textgreen (text):
  return "\033[0;32m"+text+"\033[m"

def textred (text):
  return "\033[0;31m"+text+"\033[m"

def textpurple (text):
  return "\033[0;35m"+text+"\033[m"

